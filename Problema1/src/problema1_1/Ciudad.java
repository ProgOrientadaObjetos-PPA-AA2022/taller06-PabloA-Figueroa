public class Ciudad {

    String nombre;
    String provincia;

    Ciudad() {
        nombre = "Loja";
        provincia = "Zamora";
    }

    public void establecerNombre(String n) {
        nombre = n;
    }

    public void establecerProvincia(String n) {
        provincia = n;
    }

    public String obtenerNombre() {
        return nombre;
    }

    public String obtenerProvincia() {
        return provincia;
    }

}
